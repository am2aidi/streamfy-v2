export type Language = keyof typeof translations
export type TranslationKey = keyof typeof translations['en']

export const getTranslation = (language: Language, key: TranslationKey): string => {
  if (typeof window !== 'undefined') {
    try {
      const raw = localStorage.getItem('streamfy-translation-overrides')
      if (raw) {
        const overrides = JSON.parse(raw) as Partial<Record<Language, Partial<Record<TranslationKey, string>>>>
        const custom = overrides[language]?.[key] || overrides.en?.[key]
        if (custom) return custom
      }
    } catch {
      // ignore malformed overrides
    }
  }
  const selected = translations[language] as Partial<Record<TranslationKey, string>>
  const fallback = translations.en as Record<TranslationKey, string>
  return selected[key] || fallback[key] || key
}

